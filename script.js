let firstFavNum = 10;
let secondFavNum = 8;

console.log('ACTIVITY 2');
console.log(firstFavNum > secondFavNum);    // greater than
console.log(firstFavNum < secondFavNum);    // less than
console.log(firstFavNum >= secondFavNum);   // greater than or equal
console.log(firstFavNum <= secondFavNum);   // less than or equal
console.log(firstFavNum === secondFavNum);   // strict equality
console.log(firstFavNum == secondFavNum);   // loose equality
console.log(firstFavNum !== secondFavNum);  // strict inequality
console.log(firstFavNum != secondFavNum);   // loose inequality